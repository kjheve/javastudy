package com.kh.day6.exam_P263;

public class A{
    void print() {
        System.out.println("입력값이 없습니다.");
    }
    void print(int a) {
        System.out.println("정수 입력값 : " + a);
    }
    void print(double a) {
        System.out.println("실수 입력값 : " + a);
    }
    void print(String a) {
        System.out.println("문자열 입력값 : " + a);
    }
}
